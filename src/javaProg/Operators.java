package javaProg;

public class Operators {

	public static void main(String[] args) {

		int a = 11;
		int b = 12;
		boolean x = true;
		boolean y = false;

		System.out.println(x && y);
		System.out.println(x || y);
		System.out.println(!x);
		System.out.println(!y);
		
		int c;
		c=a;     //assignment
		System.out.println(c);
		c=b;
		System.out.println(c);
		
		c=100;
		//c++; -increment
		//c--;  // decremt
		//c+=5;
		c-=5;
		System.out.println(c);

	}
}