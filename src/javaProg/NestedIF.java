package javaProg;

public class NestedIF {

	public static void main(String[] args) {
		int day = 6;

		if (day == 1) {
			System.out.println("sunday");

		}else if(day==2){
			System.out.println("monday");
		}else if(day==3) {
			System.out.println("tuesday");
		}else {
			System.out.println("friday");
		}
	}

}
